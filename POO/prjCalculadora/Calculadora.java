
public class Calculadora {
	int num1;
	int num2;
	int resultado;
	
	public void soma() {
		resultado=num1+num2;
		System.out.println(resultado);
	}
	public void subtracao() {
		resultado=num1-num2;
		System.out.println(resultado);
	}
	public void divisao() {
		resultado=num1/num2;
		System.out.println(resultado);
	}
	public void multiplicacao() {
		resultado=num1*num2;
		System.out.println(resultado);
	}
}
